#include "libft.h"

int		ft_isalpha(int c)
{
	unsigned char	sym;

	if ((c > 255) || (c < 0))
		return (0);
	sym = (unsigned char)c;
	if (!((sym >= 'a') && (sym <= 'z')))
	{
		if (!((sym >= 'A') && (sym <= 'Z')))
		{
			return (0);
		}
	}
	return (1);
}
