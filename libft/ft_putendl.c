#include "libft.h"

void	ft_putendl(char const *s)
{
	int		i;

	i = 0;
	if (s != NULL)
	{
		while (s[i] != '\0')
		{
			ft_putchar(s[i]);
			i++;
		}
		write(1, "\n", 1);
	}
}
