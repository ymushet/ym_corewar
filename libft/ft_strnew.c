#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*p;
	size_t	i;

	i = 0;
	if ((p = (char *)malloc(sizeof(char) * size + 1)))
	{
		while (i < size)
		{
			p[i] = '\0';
			i++;
		}
		p[i] = '\0';
	}
	return (p);
}
